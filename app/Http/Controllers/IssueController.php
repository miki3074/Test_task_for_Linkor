    {
        $issue = new Issue();
        $issue -> name = $request ['name'];
        $issue -> address = $request ['address'];
        $issue -> timestart = $request ['timestart'];
        $issue -> timeend = $request ['timeend'];
        $issue ->save();
        return redirect('redirects');
    }



